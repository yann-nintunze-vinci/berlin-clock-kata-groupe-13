export class Main {
    




    getLumieresMinutesSimples(minute) {
        if(minute === 4) return 'JJJJ';
        if(minute === 3) return 'JJJO';
        if(minute === 2) return 'JJOO';
        if(minute === 1) return 'JOOO';
        if(minute === 0) return 'OOOO';
    }


    
}